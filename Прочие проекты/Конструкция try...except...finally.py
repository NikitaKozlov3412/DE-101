# Конструкция try...except...finally

try:
    number = int(input("Введите число: "))
    print("Введенное число:", number)
except:
    print("Преобразование прошло неудачно")
print("Завершение программы")

print("\n")


# Блок finally

try:
    number = int(input("Введите число: "))
    print("Введенное число:", number)
except:
    print("Преобразование прошло неудачно")
finally:
    print("Блок try завершил выполнение")
print("Завершение программы")

print("\n")


'''
Стоит отметить, что блок finally не обрабатывает исключения, 
и если мы используем этот блок без блока except, то при возникновении 
ошибки приложение аварийно завершится, как в следующем случае 
при делении числа на ноль:
'''

try:
    number = 3/0    # генерирует исключение ZeroDivisionError
    print(number)
finally:
    print("Блок try завершил выполнение")
print("Завершение программы")

print("\n")