'''
Рекомендуется не тратить ресурсы впустую и следить за тем, чтобы файлы 
всегда закрывались после того, как они были использованы. Один из 
способов сделать это - использовать try и finally.
'''


try:
  f = open("игры за 30_10_2024.txt")
  cont = f.read()
  print(cont)
finally:
 f.close()
 
 '''
 Это можно также сделать, используя конструкцию with. Это создаст временную 
 переменную (часто называемую f), которая только доступна в блоке с отступом 
 в конструкции with.
 '''
 
 with open("игры за 30_10_2024.txt") as f:
  print(f.read())
  
'''
Файл автоматически закроется в конце конструкции with, даже если в нем 
возникнет исключение.
'''


