'''
Вам дан файл books.txt, который включает названия книг, каждое на новой строке.
Напишите программу для вывода количества слов в каждом названии в следующем формате:
Line 1: 3 words
Line 2: 5 words
'''

with open("игры за 30_10_2024.txt") as f:
  #ваш код
    # Проходим по каждой строке в файле
    for line_number, line in enumerate(f, start=1):
        # Убираем лишние пробелы и подсчитываем количество слов
        word_count = len(line.strip().split())
        # Выводим результат в нужном формате
        print(f"Line {line_number}: {word_count} words")
   